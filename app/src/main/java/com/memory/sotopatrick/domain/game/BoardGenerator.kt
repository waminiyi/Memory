    fun generateShuffledSymbols(gridSize: Int): List<String> {
        require(gridSize % 2 == 0) { "Grid size must be even" }
        val numberOfPairs = gridSize / 2
        require(numberOfPairs <= EMOJIS.size) {
            "Unsupported grid size: $gridSize. Maximum supported cards: ${EMOJIS.size * 2}"
        }
        val selectedSymbols = EMOJIS.shuffled().take(numberOfPairs)
        val deck = (selectedSymbols + selectedSymbols).shuffled()
        return deck
    }
